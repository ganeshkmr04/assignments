package week1.day1;

public class Maths {

	public int addTwoNumbers(int inputOne, int inputTwo) {
		return (inputOne + inputTwo);
	}
	public int multiplyTwoNumbers(int inputOne, int inputTwo) {
		return (inputOne * inputTwo);
	}
	public int divideTwoNumbers(int inputOne, int inputTwo) {
		return (inputOne / inputTwo);
	}
}
